
let x = 1;

console.log(x > 0); // true | Greater than
console.log(x >= 1); // true | Greater than or equal to
console.log(x < 1); // false | Less than
console.log(x <= 1); // true | 

console.log(x === 1); // true | Strict equality (compares value and type)
console.log(x !== 1); // false | Strict inequality (compares value and type)